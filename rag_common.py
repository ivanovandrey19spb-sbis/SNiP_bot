"""Общие подсказки для промпта LLM при RAG (оценки ретривера)."""

# Пояснение для модели: score — от ретривера, не от LLM и не вероятность.
RETRIEVAL_SCORE_EXPLANATION_RU = (
    "У каждого фрагмента указан retrieval_score — числовая оценка релевантности от модуля поиска "
    "(не вероятность и не «уверенность» языковой модели). Чем выше, тем сильнее совпадение с запросом "
    "по используемому методу в этом запуске. Сравнивайте только фрагменты между собой в этом блоке."
)


def context_header_lines(*, rag_kind: str, block_title: str) -> list[str]:
    """Заголовок блока контекста + пояснение про retrieval_score."""
    return [
        f"{block_title} (режим поиска: {rag_kind}):",
        RETRIEVAL_SCORE_EXPLANATION_RU,
        "",
    ]
